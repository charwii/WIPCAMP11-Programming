public class _3_Logical {
    public static void main(String[] args) {
        int x = 5;
        
        System.out.println("|| : " + (x < 8 || x > 4));
        
        System.out.println("&& : " + (x > 6 && x < 3));
        
        System.out.println("Normal : " + (x >= 5));
        
        System.out.println("! : " + !(x >= 5));
    }
}
